#include "3-calc.h"
/**
* op_add - operation sum two integers
* @a: first number
* @b: second number
*
* Return: result of sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - operation difference two integers
* @a: first number
* @b: second number
*
* Return: result of difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - operation multiplication two integers
* @a: first number
* @b: second number
*
* Return: result of product
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - operation division two integers
* @a: first number
* @b: second number
*
* Return: result of division
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - operation remainder of the division in two integers
* @a: first number
* @b: second number
*
* Return: result of remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
